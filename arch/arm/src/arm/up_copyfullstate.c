/* A little faster than most memcpy's */

void up_copyfullstate(uint32_t *dest, uint32_t *src)
{
  int i;

  /* In the current ARM model, the state is always copied to and from the
   * stack and TCB.
   */

  for (i = 0; i < XCPTCONTEXT_REGS; i++)
    {
      *dest++ = *src++;
    }
}

