/****************************************************************************
 * Name: up_lowputs
 *
 * Description:
 *   This is a low-level helper function used to support debug.
 *
 ****************************************************************************/

void up_lowputs(const char *str)
{
  while (*str)
    {
      up_lowputc(*str++);
    }
}
