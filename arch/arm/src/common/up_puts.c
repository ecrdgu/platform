/****************************************************************************
 * Name: up_puts
 *
 * Description:
 *   This is a low-level helper function used to support debug.
 *
 ****************************************************************************/

void up_puts(const char *str)
{
  while (*str)
    {
      up_putc(*str++);
    }
}

